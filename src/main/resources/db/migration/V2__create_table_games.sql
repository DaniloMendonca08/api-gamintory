CREATE TABLE GAMINTORY_GAMES (
    game_id INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
    user_id INT NOT NULL,
    name VARCHAR2(255) NOT NULL,
    description VARCHAR2(255),
    genre VARCHAR2(50) NOT NULL,
    stars INT CHECK ( stars IS NULL OR (stars >= 1 AND stars <=5) ),
    releaseYear INT CHECK (releaseYear >= 1958 AND releaseYear <= EXTRACT(YEAR FROM SYSDATE)),
    timePlayedInHours INT,
    gameCover varchar2(255) NOT NULL,
    addedAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT PK_GAME_ID PRIMARY KEY (game_id),
    CONSTRAINT FK_USER_ID_GAMES FOREIGN KEY (user_id) REFERENCES GAMINTORY_USERS(user_id)
);